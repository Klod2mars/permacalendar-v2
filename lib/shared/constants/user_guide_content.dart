1 — Bienvenue dans Sowing
Sowing est une application pensée pour accompagner les jardiniers et jardinières dans le suivi vivant et concret de leurs cultures.
Elle vous permet de :
• organiser vos jardins et vos parcelles,
• suivre vos plantations tout au long de leur cycle de vie,
• planifier vos tâches au bon moment,
• conserver la mémoire de ce qui a été fait,
• prendre en compte la météo locale et le rythme des saisons.
L’application fonctionne principalement hors ligne et conserve vos données directement sur votre appareil.
Cette notice décrit l’utilisation courante de Sowing : prise en main, création des jardins, plantations, calendrier, météo, export des données et bonnes pratiques.

2 — Comprendre l’interface
Le tableau de bord
À l’ouverture, Sowing affiche un tableau de bord visuel et organique.
Il se présente sous la forme d’une image de fond animée par des bulles interactives. Chaque bulle donne accès à une grande fonction de l’application :
• jardins,
• météo de l’air,
• météo du sol,
• calendrier,
• activités,
• statistiques,
• paramètres.
Navigation générale
Il suffit de toucher une bulle pour ouvrir la section correspondante.
À l’intérieur des pages, vous trouverez selon les contextes :
• des menus contextuels,
• des boutons « + » pour ajouter un élément,
• des boutons d’édition ou de suppression.

3 — Démarrage rapide
Ouvrir l’application
Au lancement, le tableau de bord s’affiche automatiquement.
Configurer la météo
Dans les paramètres, choisissez votre commune.
Cette information permet à Sowing d’afficher une météo locale adaptée à votre jardin. Si aucune commune n’est sélectionnée, une localisation par défaut est utilisée.
Créer votre premier jardin
Lors de la première utilisation, Sowing vous guide automatiquement pour créer votre premier jardin.
Vous pouvez également créer un jardin manuellement depuis le tableau de bord.
Sur l’écran principal, touchez la feuille verte située dans la zone la plus libre, à droite des statistiques et légèrement au‑dessus. Cette zone volontairement discrète permet d’initier la création d’un jardin.
Vous pouvez créer jusqu’à cinq jardins.
Cette approche fait partie de l’expérience Sowing : il n’existe pas de bouton « + » permanent et central. L’application invite plutôt à l’exploration et à la découverte progressive de l’espace.
Les zones liées aux jardins sont également accessibles depuis le menu Paramètres.
Calibration organique du tableau de bord
Un mode de calibration organique permet :
• de visualiser l’emplacement réel des zones interactives,
• de les déplacer par simple glissement du doigt.
Vous pouvez ainsi positionner vos jardins et modules exactement où vous le souhaitez sur l’image : en haut, en bas ou à l’endroit qui vous convient le mieux.
Une fois validée, cette organisation est enregistrée et conservée dans l’application.
Créer une parcelle
Dans la fiche d’un jardin :
• choisissez « Ajouter une parcelle »,
• indiquez son nom, sa surface et, si besoin, quelques notes,
• enregistrez.
Ajouter une plantation
Dans une parcelle :
• appuyez sur le bouton « + »,
• choisissez une plante dans le catalogue,
• indiquez la date, la quantité et les informations utiles,
• validez.

4 — Le tableau de bord organique
Le tableau de bord est le point central de Sowing.
Il permet :
• d’avoir une vue d’ensemble de votre activité,
• d’accéder rapidement aux fonctions principales,
• de naviguer de manière intuitive.
Selon vos réglages, certaines bulles peuvent afficher des informations synthétiques, comme la météo ou les tâches à venir.

5 — Jardins, parcelles et plantations
Les jardins
Un jardin représente un lieu réel : potager, serre, verger, balcon, etc.
Vous pouvez :
• créer plusieurs jardins,
• modifier leurs informations,
• les supprimer si nécessaire.
Les parcelles
Une parcelle est une zone précise à l’intérieur d’un jardin.
Elle permet de structurer l’espace, d’organiser les cultures et de regrouper plusieurs plantations au même endroit.
Les plantations
Une plantation correspond à l’introduction d’une plante dans une parcelle, à une date donnée.
Lors de la création d’une plantation, Sowing propose deux modes.
Semer
Le mode « Semer » correspond à la mise en terre d’une graine.
Dans ce cas :
• la progression démarre à 0 %,
• un suivi pas à pas est proposé, particulièrement utile pour les jardiniers débutants,
• une barre de progression visualise l’avancement du cycle de culture.
Ce suivi permet d’estimer :
• le début probable de la période de récolte,
• l’évolution de la culture dans le temps, de manière simple et visuelle.
Planter
Le mode « Planter » est destiné aux plants déjà développés (plants issus d’une serre ou achetés en jardinerie).
Dans ce cas :
• la plante démarre avec une progression d’environ 30 %,
• le suivi est immédiatement plus avancé,
• l’estimation de la période de récolte est ajustée en conséquence.
Choix de la date
Lors de la plantation, vous pouvez choisir librement la date.
Cela permet par exemple :
• de renseigner une plantation réalisée auparavant,
• de corriger une date si l’application n’était pas utilisée au moment du semis ou de la plantation.
Par défaut, la date du jour est utilisée.
Suivi et historique
Chaque plantation dispose :
• d’un suivi de progression,
• d’informations sur son cycle de vie,
• d’étapes de culture,
• de notes personnelles.
Toutes les actions (semis, plantation, soins, récoltes) sont automatiquement enregistrées dans l’historique du jardin.

6 — Catalogue de plantes
Le catalogue regroupe l’ensemble des plantes disponibles lors de la création d’une plantation.
Il constitue une base de référence évolutive, pensée pour couvrir les usages courants tout en restant personnalisable.
Fonctions principales :
• recherche simple et rapide,
• reconnaissance des noms courants et scientifiques,
• affichage de photos lorsque disponibles.
Plantes personnalisées
Vous pouvez créer vos propres plantes personnalisées depuis :
Paramètres → Catalogue de plantes.
Il est alors possible de :
• créer une nouvelle plante,
• renseigner les paramètres essentiels (nom, type, informations utiles),
• ajouter une image pour faciliter l’identification.
Les plantes personnalisées sont ensuite utilisables comme n’importe quelle autre plante du catalogue.

7 — Calendrier et tâches
La vue calendrier
Le calendrier affiche :
• les tâches prévues,
• les plantations importantes,
• les périodes de récolte estimées.
Créer une tâche
Depuis le calendrier :
• créez une nouvelle tâche,
• indiquez un titre, une date et une description,
• choisissez une éventuelle récurrence.
Les tâches peuvent être associées à un jardin ou à une parcelle.
Gestion des tâches
Vous pouvez :
• modifier une tâche,
• la supprimer,
• l’exporter pour la partager.

8 — Activités et historique
Cette section constitue la mémoire vivante de vos jardins.
Sélection d’un jardin
Depuis le tableau de bord, effectuez un appui long sur un jardin pour le sélectionner.
Le jardin actif est mis en évidence par une légère auréole verte et un bandeau de confirmation.
Cette sélection permet de filtrer les informations affichées.
Activités récentes
L’onglet « Activités » affiche chronologiquement :
• créations,
• plantations,
• soins,
• récoltes,
• actions manuelles.
Historique par jardin
L’onglet « Historique » présente l’historique complet du jardin sélectionné, année après année.
Il permet notamment de :
• retrouver les plantations passées,
• vérifier si une plante a déjà été cultivée à un endroit donné,
• mieux organiser la rotation des cultures.

9 — Météo de l’air et météo du sol
Météo de l’air
La météo de l’air fournit les informations essentielles :
• température extérieure,
• précipitations (pluie, neige, absence de pluie),
• alternance jour / nuit.
Ces données aident à anticiper les risques climatiques et à adapter les interventions.
Météo du sol
Sowing intègre un module de météo du sol.
L’utilisateur peut renseigner une température mesurée. À partir de cette donnée, l’application estime dynamiquement l’évolution de la température du sol dans le temps.
Cette information permet :
• de savoir quelles plantes sont réellement cultivables à un instant donné,
• d’ajuster les semis aux conditions réelles plutôt qu’à un calendrier théorique.
Météo en temps réel sur le tableau de bord
Un module central en forme d’ovoïde affiche en un coup d’œil :
• l’état du ciel,
• le jour ou la nuit,
• la phase et la position de la lune pour la commune sélectionnée.
Navigation dans le temps
En faisant glisser le doigt de gauche à droite sur l’ovoïde, vous parcourez les prévisions heure par heure, jusqu’à plus de 12 heures à l’avance.
La température et les précipitations s’ajustent dynamiquement pendant le geste.

10 — Recommandations
Sowing peut proposer des recommandations adaptées à votre situation.
Elles s’appuient sur :
• la saison,
• la météo,
• l’état de vos plantations.
Chaque recommandation précise :
• quoi faire,
• quand agir,
• pourquoi l’action est suggérée.

11 — Export et partage
Export PDF — calendrier et tâches
Les tâches du calendrier peuvent être exportées en PDF.
Cela permet de :
• partager une information claire,
• transmettre une intervention prévue,
• conserver une trace lisible et datée.
Export Excel — récoltes et statistiques
Les données de récolte peuvent être exportées au format Excel afin de :
• analyser les résultats,
• produire des bilans,
• suivre l’évolution dans le temps.
Partage des documents
Les documents générés peuvent être partagés via les applications disponibles sur votre appareil (messagerie, stockage, transfert vers un ordinateur, etc.).

12 — Sauvegarde et bonnes pratiques
Les données sont stockées localement sur votre appareil.
Bonnes pratiques recommandées :
• effectuer une sauvegarde avant une mise à jour importante,
• exporter régulièrement vos données,
• maintenir l’application et l’appareil à jour.

13 — Paramètres
Le menu Paramètres permet d’adapter Sowing à vos usages.
Vous pouvez notamment :
• choisir la langue,
• sélectionner votre commune,
• accéder au catalogue de plantes,
• personnaliser le tableau de bord.
Personnalisation du tableau de bord
Il est possible de :
• repositionner chaque module,
• ajuster l’espace visuel,
• changer l’image de fond,
• importer votre propre image (fonctionnalité à venir).
Informations légales
Depuis les paramètres, vous pouvez consulter :
• le guide d’utilisation,
• la politique de confidentialité,
• les conditions d’utilisation.

14 — Questions fréquentes
Les zones tactiles ne sont pas bien alignées
Selon le téléphone ou les réglages d’affichage, certaines zones peuvent sembler décalées.
Un mode de calibration organique permet de :
• visualiser les zones tactiles,
• les repositionner par glissement,
• enregistrer la configuration pour votre appareil.
Puis‑je utiliser Sowing sans connexion ?
Oui. Sowing fonctionne hors ligne pour la gestion des jardins, plantations, tâches et historique.
Une connexion est uniquement utilisée :
• pour la récupération des données météo,
• lors de l’export ou du partage de documents.
Aucune autre donnée n’est transmise.

15 — Remarque finale
Sowing est conçu comme un compagnon de jardinage : simple, vivant et évolutif.
Prenez le temps d’observer, de noter et de faire confiance à votre expérience autant qu’à l’outil.
